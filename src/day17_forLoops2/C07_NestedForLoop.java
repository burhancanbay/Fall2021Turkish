package day17_forLoops2;

public class C07_NestedForLoop {

	public static void main(String[] args) {
		// 1'den 4'e kadar sayilari yan yana aralarinda bir bosluk birakarak yazdirin
		
		
for (int a = 1; a <=4; a++) {
	for (int b = 1; b <=5; b++) {
		System.out.print(a*b+" ");
	}
	System.out.println("");
}
		}
	}


